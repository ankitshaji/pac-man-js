  //legend
  //0 -pack-dot
  //1 -wall
  //2 ghost-lair
  //3 -power-pellet
  //4 -empty
  //draw the grid and render it
  function createBoard() {
    for (let i = 0; i < layout.length; i++) {
      const square = document.createElement("div");
      grid.appendChild(square);
      squares.push(square);

      //add layout to the board
      if (layout[i] === 0) {
        squares[i].classList.add("pac-dot");
      } else if (layout[i] === 1) {
        squares[i].classList.add("wall");
      } else if (layout[i] === 2) {
        squares[i].classList.add("ghost-lair");
      } else if (layout[i] === 3) {
        squares[i].classList.add("power-pellet");
      }
    }
  }
  createBoard();
  //starting postion pac-man
  let packmanCurrentIndex = 490;
  squares[packmanCurrentIndex].classList.add("pac-man");

  //move pac-man(event)
  function movePacman(e) {
    squares[packmanCurrentIndex].classList.remove("pac-man");

    //arrow key codes
    switch (e.keyCode) {
      case 37:
        if (
          packmanCurrentIndex % width !== 0 &&
          !squares[packmanCurrentIndex - 1].classList.contains("wall") &&
          !squares[packmanCurrentIndex - 1].classList.contains("ghost-lair")
        )
          packmanCurrentIndex -= 1;
        //left exit
        if (squares[packmanCurrentIndex - 1] === squares[363]) {
          packmanCurrentIndex = 391;
        }
        break;
      case 38:
        if (
          packmanCurrentIndex - width >= 0 &&
          !squares[packmanCurrentIndex - width].classList.contains("wall") &&
          !squares[packmanCurrentIndex - width].classList.contains("ghost-lair")
        )
          packmanCurrentIndex -= width;
        break;
      case 39:
        if (
          packmanCurrentIndex % width < width - 1 &&
          !squares[packmanCurrentIndex + 1].classList.contains("wall") &&
          !squares[packmanCurrentIndex + 1].classList.contains("ghost-lair")
        )
          packmanCurrentIndex += 1;
        //left exit
        if (squares[packmanCurrentIndex + 1] === squares[392]) {
          packmanCurrentIndex = 364;
        }
        break;
      case 40:
        if (
          packmanCurrentIndex + width < width * width &&
          !squares[packmanCurrentIndex + width].classList.contains("wall") &&
          !squares[packmanCurrentIndex + width].classList.contains("ghost-lair")
        )
          packmanCurrentIndex += width;
        break;
    }
    squares[packmanCurrentIndex].classList.add("pac-man");
    pacDotEaten();
    powerPelletEaten();
    checkForGameOver();
    //checkForWin()
  }
  document.addEventListener("keyup", movePacman);

  function pacDotEaten() {
    if (squares[packmanCurrentIndex].classList.contains("pac-dot")) {
      score++;
      scoreDisplay.innerHTML = score;
      squares[packmanCurrentIndex].classList.remove("pac-dot");
    }
  }

  function powerPelletEaten() {
    if (squares[packmanCurrentIndex].classList.contains("power-pellet")) {
      score += 10;
      ghosts.forEach((ghost) => (ghost.isScared = true));
      setTimeout(unScareGhosts, 10000);
      squares[packmanCurrentIndex].classList.remove("power-pellet");
    }
  }
  function unScareGhosts() {
    ghosts.forEach((ghost) => (ghost.isScared = false));
  }

  //ghost class
  class Ghost {
    constructor(className, startIndex, speed) {
      this.className = className;
      this.startIndex = startIndex;
      this.speed = speed;
      this.currentIndex = startIndex;
      this.timerId = NaN;
      this.isScared = false;
    }
  }
  ghosts = [
    new Ghost("blinky", 348, 250),
    new Ghost("pinky", 376, 400),
    new Ghost("inky", 351, 300),
    new Ghost("clyde", 379, 500),
  ];

  //draw ghost

  ghosts.forEach((ghost) => {
    squares[ghost.currentIndex].classList.add(ghost.className);
    squares[ghost.currentIndex].classList.add("ghost");
  });

  //move ghost
  ghosts.forEach((ghost) => moveGhost(ghost));

  //moveGhost
  function moveGhost(ghost) {
    const directions = [-1, +1, width, -width];
    let direction = directions[Math.floor(Math.random() * directions.length)];

    ghost.timerId = setInterval(function () {
      if (
        !squares[ghost.currentIndex + direction].classList.contains("ghost") &&
        !squares[ghost.currentIndex + direction].classList.contains("wall")
      ) {
        squares[ghost.currentIndex].classList.remove(ghost.className);
        squares[ghost.currentIndex].classList.remove("ghost", "scared-ghost");
        ghost.currentIndex += direction;
        squares[ghost.currentIndex].classList.add(ghost.className, "ghost");
      } else direction = directions[Math.floor(Math.random() * directions.length)];

      if (ghost.isScared) {
        squares[ghost.currentIndex].classList.add("scared-ghost");
      }

      if (
        ghost.isScared &&
        squares[ghost.currentIndex].classList.contains("pac-man")
      ) {
        squares[ghost.currentIndex].classList.remove(
          ghost.className,
          "ghost",
          "scared-ghost"
        );
        ghost.currentIndex = ghost.startIndex;
        score += 100;
        squares[ghost.currentIndex].classList.add(ghost.className, "ghost");
      }
      checkForGameOver();
    }, ghost.speed);
  }

  function checkForGameOver() {
    if (
      squares[packmanCurrentIndex].classList.contains("ghost") &&
      !squares[packmanCurrentIndex].classList.contains("scared-ghost")
    ) {
      ghosts.forEach((ghost) => clearInterval(ghost.timerId));
      document.removeEventListener("keyup", movePacman);
      setTimeout(function () {
        alert("Game Over");
      }, 500);
    }
  }
